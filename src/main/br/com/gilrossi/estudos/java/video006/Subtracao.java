package video006;

public class Subtracao {

	//subtrair byte
	byte subtrairByte(byte num1, byte num2) {
		byte resultado = (byte) (num1 - num2);
		System.out.println(num1 + " - " + num2 + " = " + resultado);
		return resultado;
	}

	//subtrair short
	short subtrairShort(short num1, short num2) {
		short resultado = (short) (num1 - num2); 
		System.out.println(num1 + " - " + num2 + " = " + resultado);
		return resultado;
	}	

	// subtrair double
	double subtrairDouble(double num1, double num2) {
		double resultado = num1 - num2;
		System.out.println(num1 + " - " + num2 + " = " + resultado);
		return resultado;
	}

	// subtrair intenger
	int subtrairInt(int num1, int num2) {
		int resultado = num1 - num2;
		System.out.println(num1 + " - " + num2 + " = " + resultado);
		return resultado;
	}

	// subtrair float
	float subtrairFloat(float num1, float num2) {
		float resultado = num1 - num2;
		System.out.println(num1 + " - " + num2 + " = " + resultado);
		return resultado;
	}

	//subtrair long
	long subtrairLong(long num1, long num2) {
		long resultado = num1 - num2;
		System.out.println(num1 + " - " + num2 + " = " + resultado);
		return resultado;
	}
}
