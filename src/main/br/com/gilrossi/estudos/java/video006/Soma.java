package video006;

public class Soma {

	//Soma byte
	byte somaByte(byte num1, byte num2) {
		byte resultado = (byte) (num1 + num2);
		System.out.println(num1 + " + " + num2 + " = " + resultado);
		return resultado;
	}

	//Soma short
	short somaShort(short num1, short num2) {
		short resultado = (short) (num1 + num2); 
		System.out.println(num1 + " + " + num2 + " = " + resultado);
		return resultado;
	}	

	// Soma double
	double somaDouble(double num1, double num2) {
		double resultado = num1 + num2;
		System.out.println(num1 + " + " + num2 + " = " + resultado);
		return resultado;
	}

	// Soma intenger
	int somaInt(int num1, int num2) {
		int resultado = num1 + num2;
		System.out.println(num1 + " + " + num2 + " = " + resultado);
		return resultado;
	}

	// Soma float
	float somaFloat(float num1, float num2) {
		float resultado = num1 + num2;
		System.out.println(num1 + " + " + num2 + " = " + resultado);
		return resultado;
	}

	//Soma long
	long somaLong(long num1, long num2) {
		long resultado = num1 + num2;
		System.out.println(num1 + " + " + num2 + " = " + resultado);
		return resultado;
	}
}
